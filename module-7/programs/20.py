# Write a Python program to separate keys and values from a dictionary using
# keys() and values() methods.

my_dict = {
    "name" : "kapil",
    "age" : 23,
    "subj" : "python",
    "marks" : 50,
    "fees" : 62000,
    "tasks" : 3
}


keys = my_dict.keys()
values = my_dict.values()

print(keys)
print(values)

